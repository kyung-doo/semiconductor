/********************************************************************************************************
*	METHOD:PAGE
********************************************************************************************************/
function initPage(){
	init_jump_basic();
};

/****************************************************************************************
* [METHOD]:JUMP BASIC
****************************************************************************************/
function init_jump_basic(){
	$('*[data-role=ui-jump-basic]').each(function(){
		$(this).data('jumpBasic',new JumpBasicUI($(this),$('*[data-role=ui-visual-area]')));
	});
}